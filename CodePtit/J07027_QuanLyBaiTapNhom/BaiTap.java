package CodePtit.J07027_QuanLyBaiTapNhom;

public class BaiTap {
    private int stt;
    private String tenBTL;

    public BaiTap(int stt, String tenBTL) {
        this.stt = stt;
        this.tenBTL = tenBTL;
    }

    public int getStt() {
        return stt;
    }

    public String getTenBTL() {
        return tenBTL;
    }
}
