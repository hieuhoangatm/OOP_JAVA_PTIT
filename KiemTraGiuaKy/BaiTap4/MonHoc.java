package KiemTraGiuaKy.BaiTap4;

public class MonHoc {
    private String maMon;
    private String tenMonHoc;

    public MonHoc( String maMon, String tenMonHoc) {
        this.maMon = maMon;
        this.tenMonHoc = tenMonHoc;
    }

    public String getMaMon() {
        return maMon;
    }

    public String getTenMonHoc() {
        return tenMonHoc;
    }
}
